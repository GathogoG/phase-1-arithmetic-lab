// Define an array containing the set of numbers
let numbers = [5, 10, 20, 15];

// Use Math.max() to find the maximum value in the set
// The spread operator (...) is used to spread the elements of the array as arguments to Math.max()
let max = Math.max(...numbers);

// Output the maximum value
console.log(max); // Output: 20
